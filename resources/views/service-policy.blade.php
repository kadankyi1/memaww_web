         <h3>SERVICE POLICY</h3>

         <p style="color: rgb(89, 89, 89)">We enjoy bringing this service to you and we care for your laundry just like we would ours, however we still need to agree to some rules to ensure that there are no misunderstandings about the &ldquo;Memaww&rdquo; service here (referred to as &quot;we,&quot; &quot;us,&quot; &quot;our&quot;, &quot;MeMaww&quot;, or &ldquo;InfoDeFa Company Ltd&rdquo;) or your obligations. By using our service, you acknowledge and accept our Service Policy.</p>
         
         <br><strong>ITEM CLASSIFICATION</strong>
         <p style="color: rgb(89, 89, 89)">To make pricing simple, we have classified items into 2 main categories. They are:</p>
          <ul>
            <li>Lightweight Items</li>
            <li>Heavyweight Items</li>
          </ul>
         
         <p style="color: rgb(89, 89, 89)">Lightweight Items refers to any item that weighs below 2kg. They usually include shirts, trousers, skirts, shorts, handkerchiefs, pillow cases, etc. Most of the items we wear as clothes fall in this category</p>
         
         <p style="color: rgb(89, 89, 89)">Heavyweight Items refers to any item that weighs 2kg or more. They usually include duvets, bedsheets.</p>
         
         <br><strong>SERVICE TYPES</strong>
         <ul>
            <li>Wash &amp; Fold: We simply wash your items and fold them. This can either be water washed or dry cleaned.</li>
            <li>Wash &amp; Iron: We wash items and iron items, folding or putting on hangers if necessary.</li>
            <li>Just Iron: We iron items, folding or putting on hangers if necessary..</li>
         </ul>
         
         <br><strong>WASHING &amp; HANDLING</strong>
         <p style="color: rgb(89, 89, 89)">
            <strong>Feasibility Of Laundering:</strong> By utilizing our service, you acknowledge that your clothing can be dried with heat in a 
            tumble dryer or outside in the sun after being washed in water on a regular cycle. Any damage to clothes that is not 
            appropriate for this regular laundering procedure cannot be covered by us. In a similar vein, we reserve the right to 
            decline to service anything&mdash;such as severely dirty objects containing human or animal waste&mdash;that we believe 
            pose a risk to our employees. This is for our team's wellbeing and security.
         </p>
         <p style="color: rgb(89, 89, 89)">
            <strong>Ruined laundry:</strong>
            We'll use every effort to 
            prevent any damage to your laundry. Regretfully, our capabilities are restricted. We don't have time, for instance, to look 
            through your pockets to see whether you've forgotten something. Therefore, if something gets left in your clothes and gets damaged 
            or causes damage, we won't be held accountable. Similarly, we cannot promise that we will locate and return any item you phone us 
            saying you left in your laundry.
            <br><br>
            No matter how delicate our washing technique, clothes still experience regular wear and 
            tear after laundering. Therefore, we cannot be held accountable for any deterioration of the laundry, such as button loss, fading, 
            wearing out, holes, or abrasions. In a similar vein, we reserve the right to refuse cleaning any specific laundry item if we 
            believe it would cause issues.
         </p>
         <p style="color: rgb(89, 89, 89)">Laundry cleaning and drying pose additional hazards. We don't go through each item of clothing one by one before putting it in the laundry. Therefore, please do not include anything in the laundry you bring to us if it is marked as &quot;dry-clean only&quot; or otherwise inappropriate for standard washing and drying methods. </p>
         
         <p style="color: rgb(89, 89, 89)"><strong>Lost Laundry:</strong> Even though we keep meticulous records of your clothing, such as labeling each bundle with your name and ensuring that your clothes are never left unattended in our facility unless they are in a washing machine, on a folding table, or in a separate laundry cart awaiting folding, it is possible that an item will be overlooked because it would be too expensive to maintain inventory throughout the entire process. Consequently, we cannot be held accountable for any lost laundry. If you think you are missing anything from your delivery, please contact us within 24 hours of receiving your clean clothes, as we are much more likely to locate misplaced laundry if you report it promptly.</p>
         
         <br><strong>ORDERS AND PAYMENT</strong>
         <p style="color: rgb(89, 89, 89)">Orders can be placed via our website, our iOS and Android mobile apps, or by giving us a call at +233 (0)53-506-5535. Same-day order collection is anticipated for all orders. Any order that needs to be picked up more than 24 hours later must be specifically scheduled over the phone.</p>
         
         <p style="color: rgb(89, 89, 89)">If you want to cancel an order, call us at +233 (0)53-506-5535 and we'll take care of it at no extra charge. We really hope you are happy with our work, but after our pick-up team sets off, we are unable to issue a refund.</p>
         
         <p style="color: rgb(89, 89, 89)"> To make things easier, when we weigh your laundry to see if you've reached the minimum weight requirement, we round up to the nearest kilogram. Our weights are final. Should you fail to meet the required minimum weight, we will promptly advise you of any additional costs. Although we hope that clients would never refuse to pay, we do maintain the right to keep your laundry until we get complete payment.</p>
         
         <p style="color: rgb(89, 89, 89)"> Uncollected laundry for 72 hours will be disposed off usually by donation to a charity or incineration.</p>
         
         <br><strong>PRICING</strong>
         <p style="color: rgb(89, 89, 89)">For prices, kindly use our app as it will show you the price before you order, or simply call us on +233 (0)53-506-5535.</p>
         
         <br><strong>PICK-UP (COLLECTIONS) & DELIVERY SCHEDULING</strong>
         <p style="color: rgb(89, 89, 89)">MeMaww offers bicycle-based pick-up and delivery services, saving a tonne of money, pollution, and petrol. We take great pride in this facet of our business and hope that you have chosen MeMaww in part because of our bicycle pick-up and delivery service. But occasionally, conditions like pouring rain and others prevent us from using bicycles to finish deliveries. As a result, if needed, we reserve the right to pick up and deliver your clothes using more traditional means.</p>
         
         <p style="color: rgb(89, 89, 89)"> We'll try our hardest to collect your laundry at the scheduled time. We are aware that if we are late, not many people will come in. Having stated that, we cannot be held responsible for unanticipated delays. In the same way, we hope you'll make an effort to arrive on time for our deliveries. We will make every effort to arrange a new time as quickly as possible if you are unable to make it for a pick-up or delivery. Every service needs to be connected to an order in order to avoid laundry mistakes. Please use all available channels to place an order if you have more dirty clothes you would need picked up when we visit your home for a delivery.</p>
         
         <p style="color: rgb(89, 89, 89)">  When we arrive at your home and you and your laundry are nowhere to be found, it's called a No Show. You can contact us to arrange for a new pickup if there is a No Show. We will notify you of this revised pickup fee and need payment before sending a rider to you.</p>
         
         <p style="color: rgb(89, 89, 89)">
            <strong>Unattended Pick-up and Delivery</strong> 
            We are not liable for your laundry before we pick it up or after we drop it off if you ask us to leave it unattended (for example, on your porch, in a corridor, or in your foyer) or with a third party (for example, a doorman or a neighbor). You also acknowledge that the times we have on file for pick-up and drop-off are correct and true.</p>
         
         <p style="color: rgb(89, 89, 89)"></p>
         
         <br><strong>MISCELLANEOUS</strong>
         <p style="color: rgb(89, 89, 89)">
            As our business changes, we may want to modify some terms in this agreement. Therefore, we encourage you to check back frequently to familiarize yourself with any changes.</p>
         
         <p style="color: rgb(89, 89, 89)">Although we think we are generally agreeable people, we recognize that MeMaww and you may have a dispute. In this event, you agree that any legal action pertaining to this agreement, or MeMaww generally, shall be conducted in the Courts of the Republic of Ghana.</p>
         
